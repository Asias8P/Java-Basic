package objectAndClasses.MoreExercise.CompanyRoster;

public class Department {
    private String name;
    private int salary;
    private String position;
    private String email;
    private String age;


    public Department(String name, int salary, String position, String email, String age) {
        this.name = name;
        this.salary = salary;
        this.position = position;
        this.email = email;
        this.age = age;
    }
}
